/*
Assigning functions to a variable
Anonymous functions in JS */

let a = function abc(arg){
    console.log(arg)
}

a("Hello World")
/*Assigning a function to a variable is only unique to JS
Providing the name of the function is not really necessary.
Functions like these are called "Annonymous functions".
NOTE: The value of the variable assigned to the function
cannot be overridden.*/