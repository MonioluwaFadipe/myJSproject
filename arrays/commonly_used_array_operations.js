
let colors = ["Red", "Blue", "Green", "Black", "White"]

console.log(colors.join("_")) //converts the array into a string. We can make use of a custom delimiter
console.log(colors.toString()) //Also converts the array into a string

let s = "A_B_C_D_E"

console.log(s.split("_")) //converts string to array using the delimiter.

console.log(colors.reverse())

console.log(colors.slice(1, 3))

